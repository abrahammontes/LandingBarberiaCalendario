let GOOGLE_SCRIPT_URL = 'https://script.google.com/macros/s/AKfycbyyGNuQegWLV5_0jUfuxE66rqb30f6LFS5d7rhjlUh1pjRgymyOR-K6R4HBJRMuV-wO/exec';

function initCalendar() {
    const calendarDays = document.querySelector('.calendar-days');
    if (!calendarDays) return;

    const today = new Date();
    const currentMonth = today.getMonth();
    const currentYear = today.getFullYear();

    // Clear placeholder
    calendarDays.innerHTML = '';
    
    // Set Month/Year display
    updateCalendarMonthDisplay();

    // Get days in month
    const daysInMonth = new Date(currentYear, currentMonth + 1, 0).getDate();
    const firstDay = new Date(currentYear, currentMonth, 1).getDay();

    // Add empty slots for the first week
    for (let i = 0; i < firstDay; i++) {
        const empty = document.createElement('div');
        empty.className = 'calendar-day muted';
        calendarDays.appendChild(empty);
    }

    // Add days
    for (let day = 1; day <= daysInMonth; day++) {
        const dayEl = document.createElement('div');
        dayEl.className = 'calendar-day';
        dayEl.textContent = day;

        if (day === today.getDate()) {
            dayEl.classList.add('active');
            updateDateInputValue(currentYear, currentMonth, day);
            refreshAvailability(`${currentYear}-${(currentMonth + 1).toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}`);
        }

        dayEl.addEventListener('click', () => {
            document.querySelectorAll('.calendar-day').forEach(d => d.classList.remove('active'));
            dayEl.classList.add('active');

            const selectedDate = updateDateInputValue(currentYear, currentMonth, day);
            refreshAvailability(selectedDate);
        });

        calendarDays.appendChild(dayEl);
    }
}

function updateDateInputValue(year, month, day) {
    const dateInput = document.getElementById('dateInput');
    if (dateInput) {
        const m = (month + 1).toString().padStart(2, '0');
        const d = day.toString().padStart(2, '0');
        const dateStr = `${year}-${m}-${d}`;
        dateInput.value = dateStr;
        return dateStr;
    }
    return null;
}

async function refreshAvailability(selectedDate) {
    const timeSelect = document.getElementById('timeSelect');
    if (!timeSelect) return;

    console.log(`[Calendar] Fetching availability for: ${selectedDate}`);

    // Store current selection to try and restore it later
    const currentSelectedTime = timeSelect.value;

    // Show loading state
    timeSelect.disabled = true;
    const originalText = timeSelect.options[0]?.text;
    if (timeSelect.options[0]) {
        timeSelect.options[0].text = currentLang === 'es' ? 'Cargando disponibilidad...' : 'Loading availability...';
    }

    let busySlots = [];
    if (GOOGLE_SCRIPT_URL) {
        try {
            const response = await fetch(`${GOOGLE_SCRIPT_URL}?date=${selectedDate}`, {
                method: 'GET',
                mode: 'cors', // Explicitly set CORS mode
            });

            if (!response.ok) {
                if (response.status === 401 || response.status === 403) {
                    console.error('[Calendar] Permission denied. Ensure your Google Script is deployed as "Anyone" (including anonymous).');
                }
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            busySlots = await response.json();
            console.log(`[Calendar] Busy slots received:`, busySlots);
        } catch (error) {
            console.error('[Calendar] Error fetching availability (possible CORS issue):', error);
            // Optional: You could add a UI warning here
        }
    } else {
        console.warn('[Calendar] GOOGLE_SCRIPT_URL is not set.');
    }

    initTimeSlots(busySlots);

    // Try to restore previous selection if it's still available (not busy)
    if (currentSelectedTime) {
        for (let i = 0; i < timeSelect.options.length; i++) {
            if (timeSelect.options[i].value === currentSelectedTime && !timeSelect.options[i].disabled) {
                timeSelect.selectedIndex = i;
                break;
            }
        }
    }

    timeSelect.disabled = false;
    if (timeSelect.options[0]) {
        timeSelect.options[0].text = originalText || (currentLang === 'es' ? 'Seleccionar hora' : 'Select time');
    }
}

function initTimeSlots(busySlots = []) {
    const timeSelect = document.getElementById('timeSelect');
    const dateInput = document.getElementById('dateInput');
    if (!timeSelect || !dateInput) return;

    const startHour = 10; // 10 AM
    const endHour = 20; // 8 PM
    const interval = 20; // 20 minutes

    // Store placeholder
    const placeholder = timeSelect.querySelector('option[disabled]');
    timeSelect.innerHTML = '';
    if (placeholder) timeSelect.appendChild(placeholder);

    // Parse selected date safely to avoid timezone issues (YYYY-MM-DD -> local Date)
    const [year, month, day] = dateInput.value.split('-').map(Number);
    const selectedLocalDate = new Date(year, month - 1, day);

    for (let hour = startHour; hour <= endHour; hour++) {
        for (let min = 0; min < 60; min += interval) {
            // Don't show slots past 8:00 PM
            if (hour === 20 && min > 0) break;

            const timeStr = formatTime(hour, min);

            // Create a local date for the specific slot
            const slotTime = new Date(year, month - 1, day, hour, min, 0);

            // Check if slot is busy
            const isBusy = busySlots.some(slot => {
                const start = new Date(slot.start);
                const end = new Date(slot.end);
                return slotTime >= start && slotTime < end;
            });

            const option = document.createElement('option');
            option.value = timeStr;
            option.textContent = isBusy ? `${timeStr} (${currentLang === 'es' ? 'Ocupado' : 'Busy'})` : timeStr;
            if (isBusy) option.disabled = true;
            timeSelect.appendChild(option);
        }
    }
}

function formatTime(hour, min) {
    const meridian = hour >= 12 ? 'PM' : 'AM';
    const h = hour % 12 || 12;
    const m = min.toString().padStart(2, '0');
    return `${h}:${m} ${meridian}`;
}

function initSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const target = document.querySelector(targetId);
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
}

function initBooking() {
    const confirmBtn = document.querySelector('[data-i18n="btn-confirm-booking"]');
    if (!confirmBtn) return;

    confirmBtn.addEventListener('click', async () => {
        const nameInput = document.getElementById('nameInput');
        const emailInput = document.getElementById('emailInput');
        const whatsappInput = document.getElementById('whatsappInput');
        const countryCodeSelect = document.getElementById('countryCodeSelect');
        const serviceSelect = document.getElementById('serviceSelect');
        const dateInput = document.getElementById('dateInput');
        const timeSelect = document.getElementById('timeSelect');
        const honeypot = document.getElementById('website_url');

        // Anti-Bot: Honeypot check
        if (honeypot && honeypot.value !== '') {
            console.warn('[Anti-Bot] Bot attempt detected (Honeypot filled).');
            return; // Silently fail to avoid giving bots feedback
        }

        if (!nameInput.value || !emailInput.value || !whatsappInput.value || !dateInput.value || (timeSelect.selectedIndex <= 0 && timeSelect.options[0].disabled)) {
            const title = currentLang === 'es' ? 'Datos Incompletos' : 'Incomplete Data';
            const msg = currentLang === 'es' 
                ? 'Por favor completa todos los campos (nombre, email, WhatsApp, fecha y hora)' 
                : 'Please fill in all fields (name, email, WhatsApp, date and time)';
            showModal('error', title, msg);
            return;
        }

        const serviceName = serviceSelect.options[serviceSelect.selectedIndex].text;
        const selectedTime = timeSelect.value;
        const [hoursMinutes, meridian] = selectedTime.split(' ');
        const [hours, minutes] = hoursMinutes.split(':');

        let hour24 = parseInt(hours);
        if (meridian === 'PM' && hour24 !== 12) hour24 += 12;
        if (meridian === 'AM' && hour24 === 12) hour24 = 0;

        const [year, month, day] = dateInput.value.split('-').map(Number);
        const startDate = new Date(year, month - 1, day, hour24, parseInt(minutes), 0);
        
        // --- NUEVA LÓGICA DE REGISTRO SEGURO ---
        
        // Bloquear botón y mostrar carga
        confirmBtn.disabled = true;
        const originalText = confirmBtn.textContent;
        confirmBtn.textContent = currentLang === 'es' ? 'Procesando...' : 'Processing...';

        try {
            const bookingParams = new URLSearchParams({
                type: 'booking',
                name: nameInput.value,
                email: emailInput.value,
                whatsapp: (() => {
                    const countryCode = countryCodeSelect.value;
                    const phone = whatsappInput.value.replace(/\D/g, '');
                    // Mexico (+52): Requires '1' after country code for WhatsApp mobile numbers
                    if (countryCode === '+52' && phone.length === 10) {
                        return `${countryCode}1${phone}`;
                    }
                    // Argentina (+54): Requires '9' after country code
                    if (countryCode === '+54') {
                        return `${countryCode}9${phone}`;
                    }
                    return `${countryCode}${phone}`;
                })(),
                service: serviceName,
                date: startDate.toISOString(),
                duration: 20
            });

            const response = await fetch(`${GOOGLE_SCRIPT_URL}?${bookingParams.toString()}`);
            const result = await response.json();
            console.log('[Booking] API Response:', result);

            if (result.status === 'success') {
                const title = currentLang === 'es' ? '¡Cita Reservada!' : 'Appointment Booked!';
                const msg = currentLang === 'es' 
                    ? 'Tu cita ha sido registrada con éxito. Revisa tu correo para la invitación con los detalles.' 
                    : 'Your appointment was successfully registered. Check your email for the invitation details.';
                
                showModal('success', title, msg);
                nameInput.value = '';
                emailInput.value = '';
                whatsappInput.value = '';
                if (countryCodeSelect) countryCodeSelect.value = '+52';
                refreshAvailability(dateInput.value);
            } else {
                throw new Error(result.message || (currentLang === 'es' ? 'Error desconocido en el servidor' : 'Unknown server error'));
            }
        } catch (error) {
            console.error('[Booking] Error registering appointment:', error);
            let userMessage = currentLang === 'es' ? 'Hubo un error al registrar tu cita: ' : 'There was an error registering your appointment: ';
            
            if (error.name === 'TypeError') {
                userMessage += currentLang === 'es' 
                    ? '\n\nPosible error de conexión o CORS. Asegúrate de que el Google Apps Script esté implementado como "Cualquier persona" (Anonymous) y que hayas aceptado los permisos.' 
                    : '\n\nPossible connection or CORS error. Make sure Google Apps Script is deployed as "Anyone" (Anonymous) and you have accepted permissions.';
            } else {
                userMessage += (error.message || error);
            }
            const title = currentLang === 'es' ? 'Error en la Reserva' : 'Booking Error';
            showModal('error', title, userMessage);
        } finally {
            confirmBtn.disabled = false;
            confirmBtn.textContent = originalText;
        }
    });
}
