let currentLang = localStorage.getItem('lang') || 'es';

document.addEventListener('DOMContentLoaded', () => {
    initCalendar();
    initTimeSlots();
    initSmoothScroll();
    initLanguage();
    initBooking();
    initModal();
});

function initModal() {
    const closeModal = document.getElementById('closeModal');
    const modal = document.getElementById('bookingModal');
    if (closeModal && modal) {
        closeModal.addEventListener('click', () => {
            modal.classList.remove('active');
        });
        // Close on clicking outside the content
        modal.addEventListener('click', (e) => {
            if (e.target === modal) modal.classList.remove('active');
        });
    }
}

function showModal(type, title, message) {
    const modal = document.getElementById('bookingModal');
    const modalTitle = document.getElementById('modalTitle');
    const modalMessage = document.getElementById('modalMessage');
    const modalIcon = document.getElementById('modalIcon');
    
    if (!modal || !modalTitle || !modalMessage || !modalIcon) return;

    modalTitle.textContent = title;
    modalMessage.textContent = message;
    
    // Icon logic
    if (type === 'success') {
        modalIcon.innerHTML = '<span class="material-symbols-outlined" style="font-size: 3rem;">check_circle</span>';
        modalIcon.className = 'modal-icon';
    } else {
        modalIcon.innerHTML = '<span class="material-symbols-outlined" style="font-size: 3rem;">error</span>';
        modalIcon.className = 'modal-icon error';
    }

    modal.classList.add('active');
}

function initLanguage() {
    const langToggle = document.getElementById('langToggle');
    if (!langToggle) return;

    applyTranslations();

    langToggle.addEventListener('click', () => {
        currentLang = currentLang === 'es' ? 'en' : 'es';
        localStorage.setItem('lang', currentLang);
        applyTranslations();
    });
}

function applyTranslations() {
    const elements = document.querySelectorAll('[data-i18n]');
    const langToggle = document.getElementById('langToggle');

    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });

    if (langToggle) {
        langToggle.textContent = translations[currentLang]["lang-btn"];
    }

    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[currentLang][key]) {
            el.placeholder = translations[currentLang][key];
        }
    });

    // Actualizar nombre del mes en el calendario
    updateCalendarMonthDisplay();
}

function updateCalendarMonthDisplay() {
    const calendarMonth = document.getElementById('calendarMonth');
    if (!calendarMonth) return;

    const today = new Date();
    const options = { month: 'long', year: 'numeric' };
    const locale = currentLang === 'es' ? 'es-MX' : 'en-US';
    calendarMonth.textContent = today.toLocaleDateString(locale, options);
}

